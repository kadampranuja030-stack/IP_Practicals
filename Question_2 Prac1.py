# Assign an integer value 

x = 10 

print("Value:", x) 

print("Type after integer assignment:", type(x)) 

# Reassign to a string 

x = "Hello" 

print("\nValue:", x) 

print("Type after string assignment:", type(x)) 

# Reassign to a float 

x = 3.14 

print("\nValue:", x) 

print("Type after float assignment:", type(x))